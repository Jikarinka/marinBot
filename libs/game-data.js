// Data math - di-generate saat runtime
export function generateMath(mode) {
    const ops = {
        easy: { min: 1, max: 20, ops: ['+', '-'] },
        medium: { min: 1, max: 50, ops: ['+', '-', '*'] },
        hard: { min: 10, max: 100, ops: ['+', '-', '*', '/'] },
    }
    const cfg = ops[mode] || ops.easy
    const op = cfg.ops[Math.floor(Math.random() * cfg.ops.length)]
    let a = Math.floor(Math.random() * (cfg.max - cfg.min + 1)) + cfg.min
    let b = Math.floor(Math.random() * (cfg.max - cfg.min + 1)) + cfg.min

    // Pastikan hasil bersih untuk pembagian
    if (op === '/') {
        b = Math.floor(Math.random() * 9) + 1
        a = b * (Math.floor(Math.random() * 10) + 1)
    }
    // Pastikan tidak negatif untuk pengurangan
    if (op === '-' && b > a) [a, b] = [b, a]

    const result = op === '+' ? a + b : op === '-' ? a - b : op === '*' ? a * b : a / b
    return { str: `${a} ${op} ${b}`, result: String(result), bonus: mode === 'hard' ? 9999 : mode === 'medium' ? 7499 : 4999 }
}
